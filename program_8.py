# WAP to Apply Identity Operator on String/List

# For strings
a = "hello"
b = "hello"
print("String Comparison:")
print("a is b:", a is b)
print("a is not b:", a is not b)

# For lists
x = [1, 2, 3]
y = [1, 2, 3]
print("\nList Comparison:")
print("x == y:", x == y)
print("x is y:", x is y)
print("x is not y:", x is not y)
